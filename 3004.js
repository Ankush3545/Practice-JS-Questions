// program of swapping of two numbers
var a=parseInt(prompt("Enter the number a:"));
var b=parseInt(prompt("Enter the number b:"));
var temp;
console.log(`Before Swapping: value of a is: ${a}  value of b is: ${b}`);
temp=a;
a=b;
b=temp;
console.log(`After swapping: value of a is :${a}  value of b is :${b}`);


